  Widget _buildSuggestedOutfit(BuildContext context) {
    // Eƒüer √∂nerilmi≈ü bir kombin yoksa, bo≈ü bir mesaj g√∂ster
    if (_suggestedOutfit == null || _suggestedOutfit!.isEmpty) {
      return const Center(
        child: Padding(
          padding: EdgeInsets.symmetric(vertical: 20.0),
          child: Column(
            children: [
              Icon(Icons.sentiment_dissatisfied, size: 48, color: Colors.grey),
              SizedBox(height: 16),
              Text(
                'Dolaplarƒ±nƒ±zda yeterli kƒ±yafet bulunmadƒ±ƒüƒ±ndan kombin olu≈üturulamadƒ±.',
                textAlign: TextAlign.center,
              ),
              SizedBox(height: 8),
              Text(
                'L√ºtfen en az bir √ºst giyim ve bir alt giyim eklediƒüinizden emin olun.',
                textAlign: TextAlign.center,
                style: TextStyle(fontSize: 12, color: Colors.grey),
              ),
            ],
          ),
        ),
      );
    }
    
    // √ñnerilmi≈ü kƒ±yafetleri g√∂ster (en fazla 3 tane)
    final outfitItems = _suggestedOutfit!.take(3).toList();
    
    return Row(
      mainAxisAlignment: MainAxisAlignment.spaceAround,
      children: outfitItems.map((item) {
        IconData icon;
        switch (item.type) {
          case ClothingType.tShirt:
          case ClothingType.shirt:
          case ClothingType.blouse:
          case ClothingType.sweater:
            icon = Icons.checkroom_outlined;
            break;
          case ClothingType.pants:
          case ClothingType.jeans:
          case ClothingType.shorts:
          case ClothingType.skirt:
            icon = Icons.roller_skating_outlined;
            break;
          case ClothingType.jacket:
          case ClothingType.coat:
            icon = Icons.umbrella_outlined;
            break;
          default:
            icon = Icons.checkroom_outlined;
        }
        
        return _buildClothingItem(
          context,
          icon: icon,
          label: _getClothingTypeName(item.type),
          description: item.name,
          imageUrl: item.imageUrl,
        );
      }).toList(),
    );
  }
  
  Widget _buildClothingItem(
    BuildContext context, {
    required IconData icon,
    required String label,
    required String description,
    String? imageUrl,
  }) {
    return Column(
      children: [
        Container(
          width: 60,
          height: 60,
          padding: const EdgeInsets.all(12),
          decoration: BoxDecoration(
            color: Theme.of(context).colorScheme.primary.withOpacity(0.1),
            shape: BoxShape.circle,
            image: imageUrl != null 
                ? DecorationImage(
                    image: _getImageProvider(imageUrl),
                    fit: BoxFit.cover,
                  )
                : null,
          ),
          child: imageUrl == null 
              ? Icon(
                  icon,
                  size: 28,
                  color: Theme.of(context).colorScheme.primary,
                )
              : null,
        ),
        const SizedBox(height: 8),
        Text(
          label,
          style: const TextStyle(fontWeight: FontWeight.bold),
        ),
        Text(
          description,
          style: TextStyle(
            fontSize: 12,
            color: Colors.grey[600],
          ),
        ),
      ],
    );
  }
  
  ImageProvider _getImageProvider(String path) {
    if (path.startsWith('file://') || path.startsWith('/data/')) {
      // Dosya yolundaki "file://" prefixini kaldƒ±r
      final filePath = path.replaceFirst('file://', '');
      return FileImage(File(filePath));
    } else {
      return NetworkImage(path);
    }
  }
  
  Widget _buildPersonalizedSuggestions(BuildContext context) {
    // Kullanƒ±cƒ±nƒ±n t√ºm kƒ±yafetlerini al
    final clothingItemsAsyncValue = ref.watch(userClothingItemsProvider);
    
    return clothingItemsAsyncValue.when(
      data: (clothingItems) {
        if (clothingItems.isEmpty) {
          return const Center(
            child: Text('Hen√ºz dolabƒ±nƒ±za kƒ±yafet eklenmemi≈ü. Kombinler i√ßin kƒ±yafet ekleyin.'),
          );
        }
        
        // Rastgele kombinler olu≈ütur (ger√ßek bir uygulamada bu kƒ±smƒ± geli≈ütirmelisin)
        final List<List<ClothingItemModel>> suggestions = [];
        
        // Eldeki kƒ±yafetlerden birka√ß farklƒ± kombin olu≈ütur
        for (int i = 0; i < 4; i++) {
          if (clothingItems.length >= 2) {
            suggestions.add(clothingItems.take(2).toList());
          }
        }
        
        if (suggestions.isEmpty) {
          return const Center(
            child: Text('Yeterli sayƒ±da kƒ±yafet bulunamadƒ±ƒüƒ±ndan kombin √∂nerileri olu≈üturulamadƒ±.'),
          );
        }
        
        return GridView.builder(
          gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
            crossAxisCount: 2,
            crossAxisSpacing: 16,
            mainAxisSpacing: 16,
            childAspectRatio: 0.8,
          ),
          itemCount: suggestions.length,
          itemBuilder: (context, index) {
            final suggestion = suggestions[index];
            
            return Container(
              decoration: BoxDecoration(
                color: Colors.grey[200],
                borderRadius: BorderRadius.circular(12),
              ),
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Container(
                    height: 120,
                    decoration: BoxDecoration(
                      color: Colors.grey[300],
                      borderRadius: const BorderRadius.vertical(
                        top: Radius.circular(12),
                      ),
                      image: suggestion.isNotEmpty && suggestion[0].imageUrl != null
                          ? DecorationImage(
                              image: _getImageProvider(suggestion[0].imageUrl!),
                              fit: BoxFit.cover,
                            )
                          : null,
                    ),
                    child: suggestion.isEmpty || suggestion[0].imageUrl == null
                        ? Center(
                            child: Icon(
                              Icons.checkroom,
                              size: 48,
                              color: Colors.grey[700],
                            ),
                          )
                        : null,
                  ),
                  Padding(
                    padding: const EdgeInsets.all(8.0),
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Text(
                          'Kombin ${index + 1}',
                          style: const TextStyle(
                            fontWeight: FontWeight.bold,
                          ),
                        ),
                        const SizedBox(height: 4),
                        Text(
                          suggestion.map((item) => item.name).join(', '),
                          style: TextStyle(
                            fontSize: 12,
                            color: Colors.grey[700],
                          ),
                          maxLines: 2,
                          overflow: TextOverflow.ellipsis,
                        ),
                      ],
                    ),
                  ),
                ],
              ),
            );
          },
        );
      },
      loading: () => const Center(child: CircularProgressIndicator()),
      error: (error, stack) => Center(
        child: Text('Kƒ±yafet verileri alƒ±nƒ±rken hata olu≈ütu: $error'),
      ),
    );
  }
  
  String _getWeatherAdvice(WeatherModel weather) {
    if (weather.isHot) {
      return '√áok sƒ±cak, hafif giyin';
    } else if (weather.isWarm) {
      return 'Sƒ±cak, hafif giyin';
    } else if (weather.isMild) {
      return 'Ilƒ±man, orta kalƒ±nlƒ±kta giyin';
    } else if (weather.isCool) {
      return 'Serin, kalƒ±n giyin';
    } else if (weather.isCold) {
      return 'Soƒüuk, √ßok kalƒ±n giyin';
    }
    
    switch (weather.condition) {
      case WeatherCondition.rainy:
        return 'Yaƒümurlu, ≈üemsiye al';
      case WeatherCondition.stormy:
        return 'Fƒ±rtƒ±nalƒ±, dƒ±≈üarƒ± √ßƒ±kma';
      case WeatherCondition.snowy:
        return 'Karlƒ±, kalƒ±n ve su ge√ßirmez giyin';
      case WeatherCondition.windy:
        return 'R√ºzgarlƒ±, r√ºzgarlƒ±k giyin';
      case WeatherCondition.foggy:
        return 'Sisli, dikkatli ol';
      case WeatherCondition.sunny:
        return 'G√ºne≈üli, g√ºne≈ü kremi kullan';
      default:
        return 'Normal hava ko≈üullarƒ±';
    }
  }
  
